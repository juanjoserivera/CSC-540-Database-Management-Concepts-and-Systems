public class DisplayInventoryBe {
	private int partId;
	private String partName;
	private String partMake;
	private int quantity;
	private int unitPrice;
	private int minQty;
	private int minOrdThr;
	public int getPartId() {
		return partId;
	}
	public void setPartId(int partId) {
		this.partId = partId;
	}
	public String getPartName() {
		return partName;
	}
	public void setPartName(String partName) {
		this.partName = partName;
	}
	public String getPartMake() {
		return partMake;
	}
	public void setPartMake(String partMake) {
		this.partMake = partMake;
	}
	public int getQuantity() {
		return quantity;
	}
	public void setQuantity(int quantity) {
		this.quantity = quantity;
	}
	public int getUnitPrice() {
		return unitPrice;
	}
	public void setUnitPrice(int unitPrice) {
		this.unitPrice = unitPrice;
	}
	public int getMinQty() {
		return minQty;
	}
	public void setMinQty(int minQty) {
		this.minQty = minQty;
	}
	public int getMinOrdThr() {
		return minOrdThr;
	}
	public void setMinOrdThr(int minOrdThr) {
		this.minOrdThr = minOrdThr;
	}
	
	

}
